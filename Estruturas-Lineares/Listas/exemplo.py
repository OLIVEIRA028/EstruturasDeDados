# Exemplo de Lista em Python

# Criando uma lista
dados = [10, 20, 30]

# Inserindo elementos
dados.append(40)  # Adiciona no final
dados.insert(1, 15)  # Insere na posição 1

# Removendo elementos
dados.remove(20)  # Remove o valor 20
elemento = dados.pop()  # Remove o último elemento

# Acessando elementos
print("Lista final:", dados)
print("Elemento removido:", elemento)


